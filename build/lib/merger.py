"""
Step 3: Merge individual part FeatureScript snippets into a single .fs file.

This is pure deterministic logic — no AI involved.
"""
from __future__ import annotations

import textwrap
from datetime import datetime, timezone

from generator import PartResult


# FeatureScript file header — one copy at top of the merged file
_FS_HEADER = """\
FeatureScript 2399;
import(path : "onshape/std/geometry.fs", version : "2399.0");
"""

# Feature function wrapper — wraps all part bodies
_FEATURE_TEMPLATE = """\
annotation {{ "Feature Type Name" : "{feature_name}" }}
export const {func_name} = defineFeature(function(context is Context, id is Id, definition is map)
precondition
{{}}
{{
{body}
}});
"""


def merge(
    plan: dict,
    results: list[PartResult],
    *,
    feature_name: str | None = None,
) -> str:
    """
    Merge part results into a complete FeatureScript file string.

    Parameters
    ----------
    plan:         The design plan from Step 1.
    results:      The per-part FeatureScript snippets from Step 2.
    feature_name: Override the Onshape feature name shown in the UI.
                  Defaults to the assembly name from the plan.

    Returns
    -------
    Complete .fs file content as a string.
    """
    assembly_name = plan.get("assembly_name", "generated_assembly")
    description = plan.get("description", "")
    func_name = _to_camel_case(assembly_name)
    display_name = feature_name or assembly_name.replace("_", " ").title()

    # --- Collect errors and warn ---
    errors = [(r.part_id, r.error) for r in results if r.error]

    # --- Build part bodies ---
    part_sections: list[str] = []
    for result in results:
        if result.error:
            section = (
                f"    // [ERROR] Part '{result.part_id}' ({result.part_name}) failed to generate:\n"
                f"    // {result.error}\n"
            )
        else:
            # Wrap each part in its own {} block to prevent variable name conflicts
            indented = textwrap.indent(result.code.strip(), "        ")
            section = (
                f"    // ── {result.part_name} ({result.part_id}) ──────────────────\n"
                f"    {{\n"
                + indented + "\n"
                + f"    }}\n"
            )
        part_sections.append(section)

    combined_body = "\n".join(part_sections)

    # --- Assemble file ---
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    header_comment = (
        f"// Generated by fs-builder on {now}\n"
        f"// Assembly: {assembly_name}\n"
        f"// {description}\n"
        + (
            "".join(
                f"// [ERROR] Part '{pid}' failed: {err}\n"
                for pid, err in errors
            )
            if errors
            else ""
        )
        + "\n"
    )

    feature_block = _FEATURE_TEMPLATE.format(
        feature_name=display_name,
        func_name=func_name,
        body=combined_body,
    )

    return header_comment + _FS_HEADER + "\n" + feature_block


def _to_camel_case(snake: str) -> str:
    """Convert snake_case to lowerCamelCase for FeatureScript function names."""
    parts = snake.split("_")
    if not parts:
        return snake
    return parts[0] + "".join(p.capitalize() for p in parts[1:])
